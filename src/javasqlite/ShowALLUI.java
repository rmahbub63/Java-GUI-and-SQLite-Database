    public void clearField() {
        txtName.setText("");
        txtID.setText("");
        txtEmail.setText("");
        txtPhone.setText("");
    }

    public void loadData() {
        connection = JavaSqlite.connectDB();
        if (connection != null) {
            String sql = "Select name,id,phone,email from student";
            try {
                pst = connection.prepareStatement(sql);
                rs = pst.executeQuery();
                Object[] columnData = new Object[4];
                while (rs.next()) {
                    columnData[0] = rs.getString("name");
                    columnData[1] = rs.getString("id");
                    columnData[2] = rs.getString("phone");
                    columnData[3] = rs.getString("email");
                    defaultTableModel.addRow(columnData);
                }
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    connection.close();
                    pst.close();
                    rs.close();
                } catch (Exception e) {
                }
            }
        }
    }

