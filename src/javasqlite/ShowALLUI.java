    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        // TODO add your handling code here:
        connection = JavaSqlite.connectDB();
        if (connection != null) {
            String currentId = tbl.getValueAt(tbl.getSelectedRow(), 1).toString();
            String sql = "Delete from student where id = '" + currentId + "'";
            try {
                pst = connection.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Deleted");
                defaultTableModel.removeRow(tbl.getSelectedRow());
                clearField();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    connection.close();
                    pst.close();
                } catch (Exception e) {
                }
            }
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        connection = JavaSqlite.connectDB();
        if (connection != null) {
            String currentName = txtName.getText();
            String currentEmail = txtEmail.getText();
            String currentPhone = txtPhone.getText();
            String currentId = txtID.getText();
            String sql = "Update student set name = '" + currentName + "', "
                    + "email = '" + currentEmail + "', phone = '" + currentPhone + "' where id = '" + currentId + "'";
            try {
                pst = connection.prepareStatement(sql);
                pst.execute();
                JOptionPane.showMessageDialog(null, "Data Updated");
                defaultTableModel.getDataVector().removeAllElements();
                defaultTableModel.fireTableDataChanged();
                loadData();
                clearField();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e);
            } finally {
                try {
                    connection.close();
                    pst.close();
                } catch (Exception e) {
                }
            }

        }
    }//GEN-LAST:event_btnUpdateActionPerformed

